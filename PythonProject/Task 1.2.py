import random

R = 5


def is_hit(x, y, R):
    """Перевіряє, чи знаходиться точка в заштрихованій області."""
    return -R <= x <= 0 and -R <= y <= 0


# Вивід заголовку таблиці
print(f"{'№':<5}{'X':<10}{'Y':<10}{'Результат':<20}")

# Генеруємо 10 пострілів і перевіряємо попадання
for i in range(1, 11):
    x = random.uniform(-R * 1.5, R * 1.5)  # Генерація випадкових координат
    y = random.uniform(-R * 1.5, R * 1.5)

    result = "Попадання!" if is_hit(x, y, R) else "Мішень не ушкоджена"
    print(f"{i:<5}{x:<10.2f}{y:<10.2f}{result:<20}")
